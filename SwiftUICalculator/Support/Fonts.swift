import SwiftUI

extension Font {
    static func museo(size: CGFloat = 28) -> Font {
        return Font.custom("Museo-700", size: size)
    }
    
    static func montserrat(size: CGFloat = 29) -> Font {
        return Font.custom("Montserrat-Bold", size: size)
    }
    
    static func digital(size: CGFloat = 40) -> Font {
        return Font.custom("DigitalNumbers-Regular", size: size)
    }
}
